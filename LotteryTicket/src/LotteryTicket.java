
public class LotteryTicket {

	public static void main(String[] args) {

		int [][] lotteryCard = {{ 20,15,17},
								{8,7,19},
								{5,9,8}
		};
		
		
		System.out.println(lotteryCard[0][0 ]);
		
		
		for( int i = 0; i<3; i++ ){
			
			System.out.println(lotteryCard[i][i]);
				 	
		}
	} 

}
